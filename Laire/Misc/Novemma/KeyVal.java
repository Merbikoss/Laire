package Laire.Misc.Novemma;

public class KeyVal {
    private String Key;
    private String Value;
    public void setKey(String Key){
        this.Key = Key;
    }
    public String getKey(){
        return Key;
    }
    public void setValue(String Value){
        this.Value = Value;
    }
    public String getValue(){
        return Value;
    }
    public void setKeyVal(String Key, String Value){
        this.Key = Key;
        this.Value = Value;
    }
}
